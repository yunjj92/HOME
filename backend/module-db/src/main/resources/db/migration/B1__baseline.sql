CREATE TABLE sources (
                         id          integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                         name        varchar NOT NULL,
                         description varchar,
                         created_at  timestamptz NOT NULL DEFAULT now(),
                         created_by  varchar,
                         updated_at  timestamptz,
